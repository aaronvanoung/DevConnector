const mongoose = require('mongoose');
//create our schema
//take in object w all the fields 
const UserSchema = new mongoose.Schema({
    //name is set to an object has a type String and required true
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    avatar: {
        type: String
    },
    date: {
        type: Date,
        default: Date.now
    }
});

module.exports = User = mongoose.model('user', UserSchema);